# Stop stack
stop_stack() {
    log_info "Stopping Supabase stack..."
    
    cd "$PROJECT_ROOT"
    docker-compose -f docker-compose.supabase.yml stop
    
    log_success "Stack stopped"
}

# Down stack
down_stack() {
    log_info "Removing Supabase stack..."
    
    cd "$PROJECT_ROOT"
    docker-compose -f docker-compose.supabase.yml down
    
    log_success "Stack removed"
}

# Show logs
show_logs() {
    cd "$PROJECT_ROOT"
    docker-compose -f docker-compose.supabase.yml logs -f "${1:-}"
}

# Main
main() {
    local command="${1:-start}"
    
    case "$command" in
        start)
            check_prerequisites
            load_env
            generate_keys
            create_network
            pull_images
            start_stack
            wait_for_postgres
            setup_auth
            run_migrations
            show_status
            log_success "Supabase stack is ready!"
            ;;
        status)
            show_status
            ;;
        stop)
            stop_stack
            ;;
        down)
            down_stack
            ;;
        logs)
            show_logs "${2:-}"
            ;;
        migrate)
            load_env
            run_migrations
            ;;
        restart)
            stop_stack
            sleep 5
            start_stack
            wait_for_postgres
            show_status
            ;;
        generate-keys)
            load_env
            generate_keys
            ;;
        *)
            echo "Usage: $0 {start|status|stop|down|logs|migrate|restart|generate-keys}"
            exit 1
            ;;
    esac
}

main "$@"