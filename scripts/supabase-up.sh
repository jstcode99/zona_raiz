    echo "=========================================="
    echo "Service URLs"
    echo "=========================================="
    echo " Kong (HTTP):      http://localhost:${KONG_HTTP_PORT:-8000}"
    echo " Kong (HTTPS):     https://localhost:${KONG_HTTPS_PORT:-8443}"
    echo " Kong Admin:       http://localhost:${KONG_ADMIN_PORT:-8001}"
