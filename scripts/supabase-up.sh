# Create Docker network
create_network() {
    log_info "Creating Docker network..."
    
    if docker network inspect supabase_network &> /dev/null; then
        log_info "Network already exists"
    else
        docker network create --subnet="${SUBNET:-172.28.0.0/16}" supabase_network
        log_success "Network created: supabase_network"
    fi
}

# Pull images
pull_images() {
    log_info "Pulling Docker images (this may take a while)..."
    
    docker pull supabase/postgres:17.1.0.147 || true
    docker pull kong:3.4-alpine || true
    docker pull supabase/gotrue:v2.158.1 || true
    docker pull supabase/postgrest:12.0.2 || true
    docker pull supabase/realtime:v2.30.23 || true
    docker pull supabase/storage-api:v1.11.7 || true
    docker pull supabase/meta:3.1.1 || true
    
    log_success "Images pulled"
}

# Start stack
start_stack() {
    log_info "Starting Supabase stack..."
    
    cd "$PROJECT_ROOT"
    
    docker-compose -f docker-compose.supabase.yml up -d --remove-orphans
    
    log_success "Stack started"
}

# Wait for Postgres
wait_for_postgres() {
    log_info "Waiting for PostgreSQL to be ready..."
    
    local max_attempts=60
    local attempt=1
    
    while [ $attempt -le $max_attempts ]; do
        if docker exec supabase_postgres pg_isready -U "${POSTGRES_USER:-postgres}" &> /dev/null; then
            log_success "PostgreSQL is ready"
            return 0
        fi
        
        echo -n "."
        sleep 2
        attempt=$((attempt + 1))
    done
    
    log_error "PostgreSQL failed to start within ${max_attempts} attempts"
    return 1
}

# Run migrations
run_migrations() {
    log_info "Running database migrations..."
    
    # Check if migrations exist
    if [ ! -d "$PROJECT_ROOT/supabase/migrations" ]; then
        log_warn "No migrations found in supabase/migrations"
        return 0
    fi
    
    # Get list of migration files
    local migrations=($(ls -1 "$PROJECT_ROOT/supabase/migrations"/*.sql 2>/dev/null | sort))
    
    if [ ${#migrations[@]} -eq 0 ]; then
        log_info "No migrations to run"
        return 0
    fi
    
    log_info "Running ${#migrations[@]} migrations..."
    
    for migration in "${migrations[@]}"; do
        local filename=$(basename "$migration")
        log_info "  Running: $filename"
        
        docker exec -i supabase_postgres psql -U "${POSTGRES_USER:-postgres}" -d "${POSTGRES_DB:-postgres}" < "$migration" || {
            log_warn "  Migration $filename may have warnings (continuing)..."
        }
    done
    
    log_success "Migrations completed"
}

# Setup auth schema
setup_auth() {
    log_info "Setting up auth schema..."
    
    docker exec -i supabase_postgres psql -U "${POSTGRES_USER:-postgres}" -d "${POSTGRES_DB:-postgres}" <<'EOF' || true
-- Create auth schema if not exists
CREATE SCHEMA IF NOT EXISTS auth;

-- Create auth tables
CREATE TABLE IF NOT EXISTS auth.users (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    email TEXT UNIQUE NOT NULL,
    encrypted_password TEXT,
    email_confirmed_at TIMESTAMPTZ,
    created_at TIMESTAMPTZ DEFAULT now(),
    updated_at TIMESTAMPTZ DEFAULT now(),
    last_sign_in_at TIMESTAMPTZ,
    confirmed_at TIMESTAMPTZ,
    confirmation_token TEXT,
    recovery_token TEXT,
    email_change_token_new TEXT,
    email_change_token_current TEXT,
    email_change INT,
    reauthentication_token TEXT,
    is_super_admin BOOLEAN DEFAULT false,
    factor_id TEXT,
    phone TEXT,
    phone_confirmed_at TIMESTAMPTZ,
    phone_change_token TEXT,
    phone_change INT,
    metadata JSONB DEFAULT '{}'::JSONB,
    aud TEXT,
    role TEXT,
    nonce TEXT,
    nonce_sent_at TIMESTAMPTZ,
    amr_claims JSONB DEFAULT '[]'::JSONB,
    webauthn_factors JSONB DEFAULT '[]'::JSONB,
    mfa_challenges JSONB DEFAULT '[]'::JSONB,
    mfa_factors JSONB DEFAULT '[]'::JSONB
);

CREATE TABLE IF NOT EXISTS auth.refresh_tokens (
    id BIGSERIAL PRIMARY KEY,
    token TEXT UNIQUE NOT NULL,
    user_id UUID NOT NULL REFERENCES auth.users(id),
    expires_at TIMESTAMPTZ,
    created_at TIMESTAMPTZ DEFAULT now(),
    revoked BOOLEAN DEFAULT false,
    replaced_by_token TEXT,
    families TEXT,
    refresh_token_family_id TEXT
);

-- Create indexes
CREATE INDEX IF NOT EXISTS idx_users_email ON auth.users(email);
CREATE INDEX IF NOT EXISTS idx_refresh_tokens_user_id ON auth.refresh_tokens(user_id);
CREATE INDEX IF NOT EXISTS idx_refresh_tokens_token ON auth.refresh_tokens(token);
EOF

    log_success "Auth schema setup complete"
}

# Show status
show_status() {
    echo ""
    echo "=========================================="
    echo "Supabase Stack Status"
    echo "=========================================="
    echo ""
    
    cd "$PROJECT_ROOT"
    docker-compose -f docker-compose.supabase.yml ps
    
