#!/bin/bash
# ============================================
# zona_raiz - Supabase Stack Startup Script
# ============================================
# This script:
#   1. Creates required Docker network
#   2. Generates JWT/keys if not present
#   3. Pulls Docker images
#   4. Starts the Supabase stack
#   5. Waits for Postgres
#   6. Runs database migrations
#
# Usage:
#   ./scripts/supabase-up.sh           # Start stack
#   ./scripts/supabase-up.sh status   # Check status
#   ./scripts/supabase-up.sh stop     # Stop stack
#   ./scripts/supabase-up.sh down    # Stop and remove
#   ./scripts/supabase-up.sh logs    # View logs
#   ./scripts/supabase-up.sh migrate # Run migrations
# ============================================

set -euo pipefail

# Colors
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

# Paths
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"
ENV_FILE="$PROJECT_ROOT/.env.supabase"
ENV_EXAMPLE="$PROJECT_ROOT/.env.supabase"

# Functions
log_info() {
    echo -e "${BLUE}[INFO]${NC} $1"
}

log_success() {
    echo -e "${GREEN}[SUCCESS]${NC} $1"
}

log_warn() {
    echo -e "${YELLOW}[WARNING]${NC} $1"
}

log_error() {
    echo -e "${RED}[ERROR]${NC} $1"
}

# Check prerequisites
check_prerequisites() {
    if ! command -v docker &> /dev/null; then
        log_error "Docker is not installed. Please install Docker first."
        exit 1
    fi

    if ! command -v docker-compose &> /dev/null; then
        log_error "Docker Compose is not installed. Please install Docker Compose first."
        exit 1
    fi

    log_info "Prerequisites check passed"
}

# Load environment variables
load_env() {
    if [ ! -f "$ENV_FILE" ]; then
        log_warn ".env.supabase not found. Creating from template..."
        if [ -f "$ENV_EXAMPLE" ]; then
            cp "$ENV_EXAMPLE" "$ENV_FILE"
            log_warn "Please edit $ENV_FILE with your values before continuing."
            exit 1
        else
            log_error "No environment template found. Please create $ENV_FILE"
            exit 1
        fi
    fi

    # Load vars
    set -a
    source "$ENV_FILE"
    set +a

    log_info "Environment variables loaded"
}

